basic_math_operations <- function(num){
    cat("No:", num, "\n")

    sqr <- num^2
    cat("Square:",sqr,"\n" )

    sqr_root <- sqrt(num)
    cat("Sqrt_root:", sqr_root,"\n")
    
    exp <- exp(num)
    cat("Exponential:",exp,"\n")

    natural_log <- log(num)
    cat("Natural Log:",natural_log,"\n")

    log10_value <- log10(num)
    cat("log10_value:", log10_value,"\n")
    
    power <- num^3
    cat("Power:",power,"\n")

}20